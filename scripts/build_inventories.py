# ---------- INVENTORY.md ----------
def _repo_files() -> list[Path]:
    """Repo files only (tracked + untracked-but-not-ignored).

    A raw rglob walk also swept in machine-local runtime state — egg-info from
    the editable install, caches, gitignored DBs — so the committed inventory
    depended on whose machine regenerated it.
    """
    try:
        raw = subprocess.run(
            ["git", "ls-files", "-z", "--cached", "--others", "--exclude-standard"],
            cwd=ROOT,
            capture_output=True,
            check=True,
        ).stdout
        return [ROOT / f for f in raw.decode().split("\0") if f]
    except Exception:
        return [p for p in ROOT.rglob("*") if p.is_file()]


def write_inventory():
    out = [
        "# Inventory\n\n",
        "Every file in the repo, with size and a one-line purpose.\n\n",
        "_Auto-generated by `scripts/build_inventories.py`._\n\n",
        "| Path | Size (bytes) | Purpose |\n",
        "|------|---:|---|\n",
    ]
    skip = {".git", "__pycache__", ".pytest_cache", "node_modules"}
    for p in sorted(_repo_files()):
        if any(part in skip for part in p.parts):
            continue
        if not p.is_file():
            continue
        rel = p.relative_to(ROOT)
        if str(rel).startswith("docs/build_reports/"):
            purpose = "Build log / evidence report"
        elif str(rel).startswith("legacy/"):
            purpose = "Legacy package preserved verbatim"
        elif str(rel).startswith("samples/"):
            purpose = "Representative meet sample"
        elif str(rel).startswith("data/ontology/"):
            purpose = "Canonical lookup table"
        elif str(rel).startswith("data/voices/"):
            purpose = "Voice profile"
        elif str(rel).startswith("data/discovered/"):
            purpose = "Runtime discovery cache (.gitkeep)"
        elif str(rel).startswith("tests/"):
            purpose = "Test suite"
        elif str(rel).startswith("src/mediahub/"):
            purpose = _purpose_from_module(rel)
        elif rel.suffix == ".md":
            purpose = "Documentation"
        else:
            purpose = ""
        out.append(f"| `{rel}` | {p.stat().st_size} | {purpose} |\n")
    (DOCS / "INVENTORY.md").write_text("".join(out))
    print(f"INVENTORY.md: {len(out) - 5} files listed")


