# ---------- DEPENDENCY_MAP.md ----------
def write_dependency_map():
    """Compute per-package who-imports-whom inside src/mediahub/."""
    pkg_root = ROOT / "src" / "mediahub"
    dep: dict[str, set[str]] = {}
    for p in pkg_root.rglob("*.py"):
        rel = p.relative_to(pkg_root)
        if not rel.parts:
            continue
        owner = rel.parts[0]
        try:
            text = p.read_text()
        except Exception:
            continue
        for m in re.finditer(r"^\s*(?:from|import)\s+mediahub\.([a-z_]+)", text, re.MULTILINE):
            target = m.group(1)
            if target != owner:
                dep.setdefault(owner, set()).add(target)

    out = [
        "# Dependency Map\n\n",
        "Per-package internal dependencies. `A → B` means files in package `A` ",
        "import from package `B`.\n\n",
        "_Auto-generated by walking `src/mediahub/` and grepping `from mediahub.<pkg>`._\n\n",
    ]
    out.append("| Package | Depends on |\n|---|---|\n")
    for k in sorted(dep):
        deps = sorted(dep[k])
        out.append(f"| `{k}` | {', '.join(f'`{d}`' for d in deps)} |\n")

    # External requirements
    out.append("\n## External Python dependencies\n\n")
    req = ROOT / "requirements.txt"
    if req.exists():
        out.append("```\n")
        out.append(req.read_text())
        out.append("\n```\n")
    (DOCS / "DEPENDENCY_MAP.md").write_text("".join(out))
    print("DEPENDENCY_MAP.md written")


