# ---------- ENV_INVENTORY.md ----------
def env_inventory_md() -> str:
    """Build the ENV_INVENTORY.md content (pure — no file write).

    Separated from ``write_env`` so a CI test can assert the committed file is
    current (``tests/test_inventories_fresh.py``) — otherwise the inventory
    silently rots whenever an env var is added without regenerating.
    """
    # Match os.environ.get(...), os.getenv(...), environ[...] subscripts, and
    # aliased access (e.g. `import os as _os; _os.environ.get`) by anchoring on
    # `environ`/`getenv` rather than a literal `os.` prefix — the narrow old
    # pattern missed subscript and aliased reads. (Provider keys resolved
    # indirectly, like GEMINI_API_KEY/ANTHROPIC_API_KEY, are recovered by the
    # secrets_store merge below, not by this regex.)
    pat = re.compile(r"(?:environ\.get\(|getenv\(|environ\[)\s*['\"]([A-Z_][A-Z0-9_]*)['\"]")
    found: dict[str, list[str]] = {}
    for p in sorted((ROOT / "src").rglob("*.py")):
        try:
            text = p.read_text()
        except Exception:
            continue
        for m in pat.finditer(text):
            found.setdefault(m.group(1), []).append(str(p.relative_to(ROOT)))

    # Credentials resolved INDIRECTLY (via get_secret()/_resolve_key over a tuple
    # of names) never appear as a literal in an environ.get() call, so the grep
    # above misses them — including GEMINI_API_KEY / ANTHROPIC_API_KEY. Merge the
    # codebase's own source-of-truth mapping so the security-critical keys are
    # never silently absent from the inventory.
    try:
        from mediahub.web.secrets_store import _SECRET_ENV_NAMES

        for names in _SECRET_ENV_NAMES.values():
            for name in names:
                found.setdefault(name, []).append(
                    "src/mediahub/web/secrets_store.py (via get_secret)"
                )
    except Exception as exc:  # never let an import break the inventory
        print(f"  note: could not merge secrets_store mapping: {exc}", file=sys.stderr)
    out = [
        "# Environment Variable Inventory\n\n",
        "Every environment variable read by the live source.\n\n",
        "_Auto-generated by grepping `os.environ.get` / `os.getenv` in `src/`._\n\n",
        "| Var | Used in |\n",
        "|---|---|\n",
    ]
    for k in sorted(found):
        files = sorted(set(found[k]))
        out.append(
            f"| `{k}` | {', '.join(f'`{f}`' for f in files[:6])}"
            f"{' …' if len(files) > 6 else ''} |\n"
        )
    out.append("\nSee `.env.example` for descriptions and defaults.\n")
    # Hand-written security appendix (least-privilege key scoping) — kept in
    # its own file so this generator can fully own the table above while the
    # freshness test still covers the whole committed document.
    extra = DOCS / "_env_inventory_security.md"
    if extra.exists():
        out.append("\n" + extra.read_text())
    return "".join(out)


def write_env():
    content = env_inventory_md()
    (DOCS / "ENV_INVENTORY.md").write_text(content)
    print(f"ENV_INVENTORY.md: {content.count(chr(10) + '| `')} vars")


