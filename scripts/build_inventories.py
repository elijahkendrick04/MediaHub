def write_routes():
    """Dump Flask app.url_map programmatically."""
    out = [
        "# Route Inventory\n\n",
        "All Flask routes registered by `mediahub.web.create_app()`.\n\n",
        "_Auto-generated from `app.url_map` by `scripts/build_inventories.py`._\n\n",
        "| Endpoint | Methods | Rule |\n",
        "|---|---|---|\n",
    ]
    try:
        from mediahub.web.web import create_app

        app = create_app()
        for r in sorted(app.url_map.iter_rules(), key=lambda x: str(x)):
            methods = ",".join(
                sorted(m for m in (r.methods or set()) if m not in {"HEAD", "OPTIONS"})
            )
            out.append(f"| `{r.endpoint}` | `{methods}` | `{r.rule}` |\n")
    except Exception as e:
        out.append(f"\n_Error introspecting app: {e}_\n")
    (DOCS / "ROUTE_INVENTORY.md").write_text("".join(out))
    print("ROUTE_INVENTORY.md written")


