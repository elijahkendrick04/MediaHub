def write_detectors():
    out = [
        "# Detector Inventory\n\n",
        "Every achievement detector currently live, swim-only.\n\n",
        "_Auto-generated by `scripts/build_inventories.py` ",
        "(walks `recognition_swim/achievements/` + ",
        "`legacy/swim_content_v5/achievements/`)._\n\n",
    ]

    out.append("## V8 detectors (`mediahub.recognition_swim.achievements`)\n\n")
    out.append("| File | Class / function | Trigger |\n|---|---|---|\n")
    live_dir = ROOT / "src" / "mediahub" / "recognition_swim" / "achievements"
    for p in sorted(live_dir.glob("*.py")):
        if p.name == "__init__.py":
            continue
        cls = _first_class(p)
        trigger = _first_docstring_line(p)
        out.append(f"| `{p.name}` | `{cls}` | {trigger} |\n")

    out.append("\n## V5 detectors (`legacy/swim_content_v5/achievements`)\n\n")
    out.append("| File | Class / function | Trigger |\n|---|---|---|\n")
    legacy_dir = ROOT / "legacy" / "swim_content_v5" / "achievements"
    if legacy_dir.exists():
        for p in sorted(legacy_dir.glob("*.py")):
            if p.name == "__init__.py":
                continue
            cls = _first_class(p)
            trigger = _first_docstring_line(p)
            out.append(f"| `{p.name}` | `{cls}` | {trigger} |\n")

    (DOCS / "DETECTOR_INVENTORY.md").write_text("".join(out))
    print("DETECTOR_INVENTORY.md written")


def _first_class(p: Path) -> str:
    text = p.read_text()
    m = re.search(r"^class\s+(\w+)", text, re.MULTILINE)
    return m.group(1) if m else "—"


def _first_docstring_line(p: Path) -> str:
    text = p.read_text()
    m = re.search(r'"""([^\n"]+)', text)
    return m.group(1).strip() if m else ""


# ---------- PROMPT_INVENTORY.md ----------
def write_prompts():
    out = [
        "# Prompt Inventory\n\n",
        "Every Claude / LLM prompt template in the source. ",
        "Search performed across all `*.py` for triple-quoted strings ",
        "containing `system:` or `\\nUser:` markers, plus explicit ",
        "`_SYSTEM_PROMPT`/`SYSTEM`/`PROMPT` variables.\n\n",
    ]
    out.append("| File | Constant | First line of prompt |\n|---|---|---|\n")
    pat = re.compile(
        r"^\s*(_SYSTEM_PROMPT|SYSTEM|PROMPT|_PROMPT|"
        r"USER_PROMPT|VISION_PROMPT|CAPTION_PROMPT)"
        r"\s*[:=]\s*",
        re.MULTILINE,
    )
    for p in (ROOT / "src").rglob("*.py"):
        text = p.read_text()
        for m in pat.finditer(text):
            after = text[m.end() : m.end() + 600]
            # Look for the first triple-quoted string that follows.
            mq = re.search(r'(?:"""|f""")(.*?)(?:"""|""")', after, re.DOTALL)
            if not mq:
                continue
            first_line = mq.group(1).strip().splitlines()[0][:120] if mq.group(1).strip() else ""
            rel = p.relative_to(ROOT)
            out.append(f"| `{rel}` | `{m.group(1)}` | {first_line} |\n")

    out.append("\n## Where to read the full prompts\n\n")
    out.append("- AI caption — `src/mediahub/web/ai_caption.py`\n")
    out.append("- Vision creative direction — `src/mediahub/creative_brief/generator.py`\n")
    out.append("- Media description — `src/mediahub/media_library/describe.py`\n")
    out.append("- Voice render guidance — `src/mediahub/voice/multi_tone_renderer.py`\n")
    (DOCS / "PROMPT_INVENTORY.md").write_text("".join(out))
    print("PROMPT_INVENTORY.md written")


