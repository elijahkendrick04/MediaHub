    parts = rel.parts
    # src/mediahub/<pkg>/...
    if len(parts) >= 3:
        pkg = parts[2]
        m = {
            "web": "Flask UI / helpers",
            "pipeline": "Upload→cards orchestrator",
            "interpreter": "Format-agnostic ingest",
            "recognition": "Sport-agnostic detector bus",
            "recognition_swim": "Swim achievement detectors",
            "pb_discovery": "PB lookup engine",
            "context_engine": "Identity + ontology + trust",
            "voice": "Caption voice profiles",
            "brand": "Brand kit logic",
            "workflow": "Card status persistence",
            "club_platform": "Content type registry",
            "graphic_renderer": "HTML→PNG renderer",
            "creative_brief": "LLM creative direction",
            "media_ai": "LLM + cutout providers",
            "media_library": "Asset store",
            "media_requirements": "Content-type → media rules",
            "venue_search": "Pool venue image search",
            "inspiration": "Pattern + exemplar library",
            "content_pack": "Pack ZIP builder",
            "content_pack_visual": "Visual pack integration",
            "web_research": "Web research client",
        }
        return m.get(pkg, "MediaHub source module")
    return "MediaHub source"


